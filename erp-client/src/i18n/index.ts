export function useI18n() {
  const preferencesStore = usePreferencesStore()
  const locale = computed(() => preferencesStore.language)

  function t(path: string) {
    return resolveMessage(locale.value, path)
  }

  function group(groupName: string) {
    return GROUP_LABELS[locale.value][groupName] || groupName
  }

  function moduleTitle(moduleKey: ModuleKey) {
    if (locale.value === 'en-US') {
      return moduleManifestMap[moduleKey]?.title || moduleKey
    }
    return MODULE_TITLES_ZH[moduleKey] || moduleManifestMap[moduleKey]?.title || moduleKey
  }

  function moduleDescription(moduleKey: ModuleKey) {
    if (locale.value === 'en-US') {
      return `${moduleManifestMap[moduleKey]?.title || moduleKey} module.`
    }
    return `${moduleTitle(moduleKey)}模块。`
  }

  function fieldLabel(fieldKey: string, fallback: string) {
    return FIELD_LABELS[locale.value][fieldKey] || fallback
  }

  function actionLabel(label: string) {
    return ACTION_LABELS[locale.value][label] || label
  }

  function routeTitle(fallback: string, moduleKey?: ModuleKey) {
    if (moduleKey) {
      return moduleTitle(moduleKey)
    }
    if (locale.value === 'en-US') {
      if (fallback === '架构总览') return 'Dashboard'
      if (fallback === '系统设置') return 'Settings'
      if (fallback === '关于系统') return 'About'
    }
    return fallback
  }

  return {
    actionLabel,
    fieldLabel,
    group,
    locale,
    moduleDescription,
    moduleTitle,
    routeTitle,
    t,
  }
}

function resolveMessage(locale: LanguagePreference, path: string) {
  const segments = path.split('.')
  let current: string | DictRecord = MESSAGES[locale] as DictRecord
  for (const segment of segments) {
    if (typeof current !== 'object' || current === null || !(segment in current)) {
      return path
    }
    current = current[segment]
  }
  return typeof current === 'string' ? current : path
}
